create or replace function public.normalize_username(p_username text)
returns text
language sql
immutable
as $$
  select lower(regexp_replace(trim(coalesce(p_username, '')), '[^a-z0-9._-]', '', 'g'));
$$;

create or replace function public.generate_hike_id()
returns text
language plpgsql
as $$
declare
  candidate text;
begin
  loop
    candidate := 'HIK-' || upper(substr(translate(encode(gen_random_bytes(5), 'base64'), '+/=', ''), 1, 6));
    exit when not exists (select 1 from public.registrations where hike_id = candidate);
  end loop;
  return candidate;
end;
$$;

