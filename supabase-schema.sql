-- Admin login
create or replace function public.admin_login(p_username text, p_password text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_record public.admins%rowtype;
begin
  select * into v_record from public.admins where username = p_username;
  if v_record.id is null then
    return jsonb_build_object('success', false, 'error', 'Invalid username or password');
  end if;
  if v_record.password_hash != crypt(p_password, v_record.password_hash) then
    return jsonb_build_object('success', false, 'error', 'Invalid username or password');
  end if;
  return jsonb_build_object('success', true, 'username', v_record.username, 'display_name', v_record.display_name);
end;
$$;
grant execute on function public.admin_login(text, text) to anon, authenticated;

-- Add admin
create or replace function public.add_admin(p_username text, p_password text, p_display_name text default '')
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.admins (username, password_hash, display_name)
  values (p_username, crypt(p_password, gen_salt('bf')), p_display_name);
  return jsonb_build_object('success', true);
exception when unique_violation then
  return jsonb_build_object('success', false, 'error', 'Username already exists');
end;
$$;
grant execute on function public.add_admin(text, text, text) to anon, authenticated;

