create or replace function public.generate_hike_id()
returns text
language plpgsql
as $$
declare
  candidate text;
begin
  loop
    candidate := 'HIK-' || upper(substr(translate(encode(gen_random_bytes(5), 'base64'), '+/=', ''), 1, 6));
    exit when not exists (select 1 from public.registrations where hike_id = candidate);
  end loop;
  return candidate;
end;
$$;

update public.registrations
set hike_id = public.generate_hike_id()
where hike_id is null or hike_id = '';

alter table public.registrations alter column hike_id set default public.generate_hike_id();
alter table public.registrations alter column hike_id set not null;

