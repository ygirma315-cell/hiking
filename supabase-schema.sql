create or replace function public.create_booking(
  p_full_name text,
  p_phone text,
  p_age int,
  p_participants_count int,
  p_gender text,
  p_destination text,
  p_package_name text,
  p_trip_date text,
  p_price numeric,
  p_currency text,
  p_payment_method text,
  p_sender_account text,
  p_transaction_id text
)
returns table (
  id bigint,
  hike_id text,
  user_id uuid,
  username text,
  full_name text,
  phone text,
  participants_count int,
  destination text,
  package_name text,
  trip_date text,
  price numeric,
  currency text,
  payment_method text,
  sender_account text,
  transaction_id text,
  status text,
  payment_status text,
  admin_message text,
  created_at timestamptz,
  updated_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_username text;
  v_payment_status text;
  v_payload jsonb;
  v_package jsonb;
  v_price numeric;
  v_currency text;
begin
  if v_user is null then
    raise exception 'Login is required to create a booking.';
  end if;

  select profiles.username into v_username
  from public.profiles
  where profiles.user_id = v_user;

  if v_username is null then
    v_username := public.normalize_username(coalesce(auth.jwt() -> 'user_metadata' ->> 'username', split_part(auth.jwt() ->> 'email', '@', 1)));
    insert into public.profiles (user_id, username, phone)
    values (v_user, v_username, coalesce(nullif(p_phone, ''), ''))
    on conflict (user_id) do nothing;
  end if;

  v_payment_status := case
    when coalesce(nullif(p_sender_account, ''), nullif(p_transaction_id, '')) is not null then 'submitted'
    else 'pending'
  end;

  select site_content.payload into v_payload
  from public.site_content
  where site_content.id = 'main';

  select package_item into v_package
  from jsonb_array_elements(jsonb_build_array(
    v_payload #> '{packages,nativeDay}',
    v_payload #> '{packages,nativeOvernight}',
    v_payload #> '{packages,foreignerDay}',
    v_payload #> '{packages,foreignerOvernight}'
  )) as packages(package_item)
  where package_item is not null
    and package_item ->> 'name' = p_package_name
  limit 1;

  v_price := coalesce(nullif(v_package ->> 'price', '')::numeric, p_price, 0);
  v_currency := coalesce(nullif(v_package ->> 'currency', ''), nullif(p_currency, ''), 'ETB');

  return query
  insert into public.registrations (
    user_id,
    username,
    full_name,
    phone,
    age,
    participants_count,
    gender,
    destination,
    package_name,
    trip_date,
    price,
    currency,
    payment_method,
    sender_account,
    transaction_id,
    payment_status,
    status
  )
  values (
    v_user,
    v_username,
    trim(p_full_name),
    trim(p_phone),
    p_age,
    greatest(1, coalesce(p_participants_count, 1)),
    p_gender,
    p_destination,
    p_package_name,
    coalesce(p_trip_date, ''),
    v_price,
    v_currency,
    coalesce(nullif(p_payment_method, ''), 'Not selected'),
    coalesce(p_sender_account, ''),
    coalesce(p_transaction_id, ''),
    v_payment_status,
    'pending'
  )
  returning
    registrations.id,
    registrations.hike_id,
    registrations.user_id,
    registrations.username,
    registrations.full_name,
    registrations.phone,
    registrations.participants_count,
    registrations.destination,
    registrations.package_name,
    registrations.trip_date,
    registrations.price,
    registrations.currency,
    registrations.payment_method,
    registrations.sender_account,
    registrations.transaction_id,
    registrations.status,
    registrations.payment_status,
    registrations.admin_message,
    registrations.created_at,
    registrations.updated_at;
end;
$$;

create or replace function public.submit_booking_payment(
  p_hike_id text,
  p_sender_account text,
  p_transaction_id text
)
returns table (
  id bigint,
  hike_id text,
  status text,
  payment_status text,
  sender_account text,
  transaction_id text,
  admin_message text,
  updated_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
begin
  if v_user is null then
    raise exception 'Login is required to submit payment details.';
  end if;

  return query
  update public.registrations r
  set
    sender_account = coalesce(p_sender_account, ''),
    transaction_id = coalesce(p_transaction_id, ''),
    payment_status = 'submitted',
    status = case when r.status in ('accepted', 'rejected') then r.status else 'pending' end
  where r.hike_id = p_hike_id
    and r.user_id = v_user
  returning
    r.id,
    r.hike_id,
    r.status,
    r.payment_status,
    r.sender_account,
    r.transaction_id,
    r.admin_message,
    r.updated_at;
end;
$$;

