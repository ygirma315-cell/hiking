-- Function: create/get linked Supabase Auth user for an admin
create or replace function public.setup_admin_auth_user(p_admin_username text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid;
  v_email text;
  v_password text := 'ereft_admin_supabase_2024';
begin
  v_email := p_admin_username || '@admin.ereft.local';

  select id into v_user_id from auth.users where email = v_email;

  if v_user_id is null then
    insert into auth.users (
      id, email, encrypted_password, email_confirmed_at,
      raw_user_meta_data, created_at, updated_at,
      confirmation_token, confirmation_sent_at,
      recovery_token, email_change,
      email_change_token_new, email_change_token_current,
      instance_id, aud, role,
      is_super_admin, is_sso_user
    ) values (
      gen_random_uuid(), v_email, crypt(v_password, gen_salt('bf')), now(),
      jsonb_build_object('username', p_admin_username), now(), now(),
      '', now(), '', '', '', '',
      '00000000-0000-0000-0000-000000000000'::uuid, 'authenticated', 'authenticated',
      false, false
    )
    returning id into v_user_id;

    insert into public.admin_users (user_id, email)
    values (v_user_id, v_email)
    on conflict (user_id) do nothing;

    insert into public.profiles (user_id, username, phone)
    values (v_user_id, p_admin_username, '')
    on conflict (user_id) do nothing;
  end if;

  update public.admin_credentials
  set supabase_user_id = v_user_id
  where username = p_admin_username;

  return jsonb_build_object('success', true, 'user_id', v_user_id::text, 'email', v_email);
end;
$$;

grant execute on function public.setup_admin_auth_user(text) to anon, authenticated;

-- Function: admin login (checks admin_credentials, NOT Supabase Auth)
create or replace function public.admin_login(p_username text, p_password text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_record public.admin_credentials%rowtype;
begin
  select * into v_record from public.admin_credentials where username = p_username;

  if v_record.id is null then
    return jsonb_build_object('success', false, 'error', 'Invalid username or password');
  end if;

  if v_record.password_hash != crypt(p_password, v_record.password_hash) then
    return jsonb_build_object('success', false, 'error', 'Invalid username or password');
  end if;

  return jsonb_build_object(
    'success', true,
    'username', v_record.username,
    'display_name', v_record.display_name
  );
end;
$$;

grant execute on function public.admin_login(text, text) to anon, authenticated;

