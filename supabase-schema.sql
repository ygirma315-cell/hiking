-- Admin login.
create or replace function public.admin_login(p_username text, p_password text)
returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_admin public.admins%rowtype;
  v_token uuid;
begin
  select * into v_admin
  from public.admins
  where username = public.normalize_login_name(p_username);

  if v_admin.id is null or v_admin.password_hash <> extensions.crypt(coalesce(p_password, ''), v_admin.password_hash) then
    return jsonb_build_object('success', false, 'error', 'Invalid username or password');
  end if;

  insert into public.admin_sessions (admin_id) values (v_admin.id)
  returning token into v_token;

  update public.admins set last_login = now() where id = v_admin.id;

  return jsonb_build_object(
    'success', true,
    'session_token', v_token,
    'admin', jsonb_build_object(
      'id', v_admin.id,
      'username', v_admin.username,
      'display_name', coalesce(nullif(v_admin.display_name, ''), v_admin.username)
    )
  );
end;
$$;

