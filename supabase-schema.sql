create or replace function public.normalize_login_name(value text)
returns text
language sql
immutable
as $$
  select lower(regexp_replace(trim(coalesce(value, '')), '[^a-zA-Z0-9._-]', '', 'g'));
$$;

create or replace function public.generate_hike_id()
returns text
language plpgsql
as $$
declare
  candidate text;
begin
  loop
    candidate := 'HIK-' || upper(substr(translate(encode(gen_random_bytes(5), 'base64'), '+/=', ''), 1, 6));
    exit when not exists (select 1 from public.registrations where hike_id = candidate);
  end loop;
  return candidate;
end;
$$;

update public.registrations
set hike_id = public.generate_hike_id()
where hike_id is null or hike_id = '';

alter table public.registrations alter column hike_id set default public.generate_hike_id();
alter table public.registrations alter column hike_id set not null;

create or replace function public.valid_admin(p_admin_token uuid)
returns bigint
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_admin_id bigint;
begin
  select admin_id into v_admin_id
  from public.admin_sessions
  where token = p_admin_token
    and expires_at > now();

  if v_admin_id is null then
    raise exception 'Admin login required.';
  end if;

  return v_admin_id;
end;
$$;

create or replace function public.valid_site_user(p_session_token uuid)
returns bigint
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_user_id bigint;
begin
  select user_id into v_user_id
  from public.site_user_sessions
  where token = p_session_token
    and expires_at > now();

  if v_user_id is null then
    raise exception 'Login is required.';
  end if;

  return v_user_id;
end;
$$;

