create or replace function public.admin_update_registration(
  p_admin_token uuid,
  p_registration_id bigint,
  p_status text default null,
  p_admin_message text default null
)
returns boolean
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_payment_status text;
begin
  perform public.valid_admin(p_admin_token);

  if p_status is not null and p_status not in ('pending', 'accepted', 'rejected', 'needs_review') then
    raise exception 'Invalid registration status.';
  end if;

  v_payment_status := case
    when p_status = 'accepted' then 'confirmed'
    when p_status = 'rejected' then 'failed'
    when p_status = 'needs_review' then 'submitted'
    when p_status = 'pending' then 'pending'
    else null
  end;

  update public.registrations
  set
    status = coalesce(p_status, status),
    payment_status = coalesce(v_payment_status, payment_status),
    admin_message = case when p_admin_message is null then admin_message else p_admin_message end
  where id = p_registration_id;

  return found;
end;
$$;

