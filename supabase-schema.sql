-- Admin function: delete a user by their user_id (cascades to profiles, admin_users, etc.)
create or replace function public.delete_user(p_user_id uuid)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists (select 1 from public.admin_users where user_id = auth.uid()) then
    raise exception 'Only admins can delete users';
  end if;
  if p_user_id = auth.uid() then
    raise exception 'You cannot delete your own account';
  end if;
  if exists (select 1 from public.admin_users where user_id = p_user_id) then
    raise exception 'Cannot delete an admin user. Remove from admin_users first.';
  end if;
  delete from auth.users where id = p_user_id;
  return found;
end;
$$;

grant execute on function public.delete_user(uuid) to authenticated;

-- Backfill: create profiles for existing auth users who signed up before the trigger existed
insert into public.profiles (user_id, username, phone)
select
  au.id,
  public.normalize_username(coalesce(au.raw_user_meta_data ->> 'username', split_part(au.email, '@', 1))),
  coalesce(au.raw_user_meta_data ->> 'phone', '')
from auth.users au
left join public.profiles p on p.user_id = au.id
where p.user_id is null
on conflict (user_id) do nothing;

