-- Main content edited by admin and read by public website.
create table if not exists public.site_content (
  id text primary key default 'main',
  payload jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

create table if not exists public.site_defaults (
  id text primary key default 'main',
  payload jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

-- Admin accounts are only for admin/index.html.
create table if not exists public.admins (
  id bigint generated by default as identity primary key,
  username text not null unique,
  password_hash text not null,
  display_name text not null default '',
  last_login timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.admin_sessions (
  token uuid primary key default extensions.gen_random_uuid(),
  admin_id bigint not null references public.admins(id) on delete cascade,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null default now() + interval '30 days'
);

-- Public website accounts are only for index.html signup/login.
create table if not exists public.site_users (
  id bigint generated by default as identity primary key,
  username text not null unique,
  password_hash text not null,
  phone text default '',
  last_login timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.site_user_sessions (
  token uuid primary key default extensions.gen_random_uuid(),
  user_id bigint not null references public.site_users(id) on delete cascade,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null default now() + interval '60 days'
);

-- Bookings/registrations created by logged-in public users.
create table if not exists public.registrations (
  id bigint generated by default as identity primary key,
  hike_id text,
  user_id bigint references public.site_users(id) on delete set null,
  username text,
  full_name text not null,
  phone text not null,
  age int,
  participants_count int not null default 1,
  gender text,
  destination text not null,
  package_name text not null,
  trip_date text default '',
  price numeric(12,2) not null default 0,
  currency text not null default 'ETB',
  payment_method text default 'Not selected',
  sender_account text default '',
  transaction_id text default '',
  payment_status text not null default 'pending',
  status text not null default 'pending',
  lookup_token uuid not null default extensions.gen_random_uuid(),
  admin_message text default '',
  notes text default '',
  submitted_date date not null default current_date,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.registrations drop constraint if exists registrations_status_check;
alter table public.registrations drop constraint if exists registrations_payment_status_check;
update public.registrations set payment_status = 'confirmed' where payment_status = 'paid';
update public.registrations set status = 'pending' where status not in ('pending', 'accepted', 'rejected', 'needs_review');
update public.registrations set payment_status = 'pending' where payment_status not in ('pending', 'submitted', 'confirmed', 'failed', 'refunded');
alter table public.registrations add constraint registrations_status_check check (status in ('pending', 'accepted', 'rejected', 'needs_review'));
alter table public.registrations add constraint registrations_payment_status_check check (payment_status in ('pending', 'submitted', 'confirmed', 'failed', 'refunded'));

